std::string toChars(int ch) {
    std::string chars;
    unsigned int chu = static_cast<unsigned int>(ch);
    for (size_t i = 0; i < sizeof(chu); ++i)
    {
        char ch = static_cast<char>((chu >> (i * CHAR_BIT)) & ((1u << CHAR_BIT) - 1u));
        if (!ch) break;
        chars.push_back(ch);
    }
    std::reverse(chars.begin(), chars.end());
    return chars;
}

