ManoTranslator::string ManoTranslator::Encode(const ManoTranslator::string& str) const
{
    ManoTranslator::string result;

    for (auto w : str)
    {
        if (m_encoder.count(w) == 0)
        {
            result += w;
            continue;
        }
        result += m_encoder.at(w);
    }

    return result;
}

